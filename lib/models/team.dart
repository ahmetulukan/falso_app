class Team {
  final String id;
  final String name;
  final String logoUrl;
  final String city;
  final String country;
  final String league;
  final String stadium;

  Team({
    required this.id,
    required this.name,
    required this.logoUrl,
    required this.city,
    required this.country,
    required this.league,
    required this.stadium,
  });
}
